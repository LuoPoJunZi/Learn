function z = MOP2(x)
    % MOP2 函数用于计算多目标优化问题中的两个目标函数值
    % 输入：
    %   x - 决策变量向量
    % 输出：
    %   z - 目标函数值的向量 [z1; z2]

    % 获取决策变量的数量
    n = numel(x);

    % 计算第一个目标函数值 z1
    % 该目标函数表示一个典型的鞍型函数，用指数函数和平方和表示
    z1 = 1 - exp(-sum((x - 1/sqrt(n)).^2));

    % 计算第二个目标函数值 z2
    % 该目标函数与 z1 类似，唯一的区别是平方项中的加减号相反
    z2 = 1 - exp(-sum((x + 1/sqrt(n)).^2));

    % 将 z1 和 z2 作为列向量返回
    z = [z1; z2];
end

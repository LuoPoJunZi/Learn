% -------------------------------------------------------------
% 文件名: ZDT1.m
% 功能: 计算ZDT1多目标优化问题的目标函数值
%       ZDT1是一个经典的多目标优化测试函数，具有两个目标函数
% 输入:
%       x - 决策变量向量（行向量或列向量）
% 输出:
%       o - 目标函数值向量，包含两个目标函数值
% 作者: [您的名字]
% 日期: [日期]
% -------------------------------------------------------------

function o = ZDT1(x)
    % 初始化输出向量，包含两个目标函数值
    o = [0, 0];
    
    % 获取决策变量的维度
    dim = length(x);
    
    % 计算辅助函数g，根据ZDT1定义，g = 1 + 9 * sum(x_2到x_n) / (n - 1)
    g = 1 + 9 * sum(x(2:dim)) / (dim - 1);
    
    % 计算第一个目标函数f1 = x1
    o(1) = x(1);
    
    % 计算第二个目标函数f2 = g * (1 - sqrt(x1 / g))
    o(2) = g * (1 - sqrt(x(1) / g));
end

% -------------------------------------------------------------
% 文件名: dominates.m
% 功能: 判断解向量x是否支配解向量y
%       支配关系定义：
%           对于所有目标，x的值小于或等于y的值，
%           并且至少在一个目标上x的值严格小于y的值。
% 输入:
%       x - 第一个解向量（行向量或列向量）
%       y - 第二个解向量（行向量或列向量）
% 输出:
%       o - 布尔值，若x支配y则为true，否则为false
% 作者: [您的名字]
% 日期: [日期]
% -------------------------------------------------------------

function o = dominates(x, y)
    % 检查x在所有目标上是否小于或等于y
    condition1 = all(x <= y);
    
    % 检查x在至少一个目标上是否严格小于y
    condition2 = any(x < y);
    
    % 如果同时满足上述两个条件，则x支配y
    o = condition1 && condition2;
end

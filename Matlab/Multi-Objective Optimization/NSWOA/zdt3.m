%% Zitzler3 函数（ZDT3）
function f = zdt3(x)
% 此函数计算 ZDT3 测试问题的目标函数值。
% 输入：
% x - 决策变量向量，长度为30，取值范围 [0,1]
% 输出：
% f - 目标函数值向量，包括两个目标值 f(1) 和 f(2)

% 初始化目标函数值
f = [];

% 决策变量的数量
n = length(x);

% 计算 g 函数
g = 1 + 9 * sum(x(2:n)) / (n - 1);

% 计算目标函数值
f(1) = x(1); % 第一个目标函数值
f(2) = 1 - sqrt(x(1) / g) - (x(1) / g) * sin(10 * pi * x(1)); % 第二个目标函数值

end

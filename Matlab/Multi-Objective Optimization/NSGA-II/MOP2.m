function z = MOP2(x)
    % MOP2：测试问题的目标函数，用于多目标优化问题
    % 输入：
    %   x - 决策变量向量
    % 输出：
    %   z - 包含两个目标函数值的列向量

    n = numel(x);  % 获取决策变量的个数
    
    % 目标函数1：一个基于平方和的函数，目标是最小化
    z1 = 1 - exp(-sum((x - 1/sqrt(n)).^2));
    
    % 目标函数2：另一个基于平方和的函数，目标是最小化
    z2 = 1 - exp(-sum((x + 1/sqrt(n)).^2));
    
    % 返回两个目标函数值组成的列向量
    z = [z1 z2]';

end
